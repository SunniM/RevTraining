x = int(input("Enter x value: "))
y = int(input("Enter y value: "))

p = x + y # Addition
q = x - y # Subtraction
r = x * y # Multiplication
s = x / y # Division
t = x % y # Modulo

print(f"{x} + {y} = {p}")
print(f"{x} - {y} = {q}")
print(f"{x} * {y} = {r}")
print(f"{x} / {y} = {s}")
print(f"{x} % {y} = {t}")
